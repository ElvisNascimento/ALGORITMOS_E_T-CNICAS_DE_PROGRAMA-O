// 7) Faça um algoritmo que leia uma variável e some 5 caso seja par ou some 8 caso seja ímpar,
// imprimir o resultado desta operação.

let meuNumero=255;

if(meuNumero % 2 === 0){
    let resultado = meuNumero + 5;
    document.write(resultado);
}else{
    let resultado = meuNumero + 8;
    document.write(resultado);
}